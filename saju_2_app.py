def get_ganji_from_solar(year: int, month: int, day: int):
    """korean_lunar_calendarë¡œ ì–‘ë ¥ â†’ ì—°/ì›”/ì¼ ê°„ì§€ ë¬¸ìì—´ ì–»ê¸°."""
    cal = KoreanLunarCalendar()
    ok = cal.setSolarDate(year, month, day)
    if not ok:
        return None

    gapja = cal.getGapJaString()  # ì˜ˆ: "ì •ìœ ë…„ ë³‘ì˜¤ì›” ì„ì˜¤ì¼"
    parts = gapja.split()

    year_ganji = parts[0][:-1]   # 'ì •ìœ '
    month_ganji = parts[1][:-1]  # 'ë³‘ì˜¤'
    day_ganji = parts[2][:-1]    # 'ì„ì˜¤'

    return year_ganji, month_ganji, day_ganji, gapja

